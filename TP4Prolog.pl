get_presidente(Num, Presidente, D, H) :- presidente_desde_hasta(Num, Presidente, D , H).

get_presidente_by_numero() :- write('Ingrese el numero de presidente que desea conocer: '), read(Num),
    get_presidente(Num, Pres,_,_), nl, write('El presidente que buscado es '), write(Pres).

período_presidencia_de(NumPres):- presidente_desde_hasta(Presidente,AD,AH), write('Desde: '),write(AD),nl,write('Hasta: '),write(AH).

acontecimiento_importante(Anio):-acontecimiento_año(Ac,Anio),write(Ac).

presidente_factico() :- write('Ingrese el número de presidente (De 1 a 55) '), read(Num), 
    get_presidente(Num, Pres, Desde, Hasta), periodo_dictadura_desde_hasta(_, DicDesde, DicHasta),
    Desde >= DicDesde, Hasta =< DicHasta, 
    get_presidente(Num, Pres, _, _), write('El presidente buscado '), write(Pres), 
    write(' fue un presidente fáctico.'), !.

presidente_factico() :- write('El presidente buscado fue elegido democraticamente').

calcular_probabilidad(NumPres, NumAcont, Prob) :- get_presidente(NumPres,Pres, D, H), AnioProm is (D + H)/ 2,
    acontecimiento_año(NumAcont, _, AnioAcon), abs(AnioProm - AnioAcon, Distancia), Prob is (1 - (Distancia / 210)) * 100.

probabilidad_presidente_este_involucrado_en_acontecimiento() :- write('Ingrese un número de presidente (De 1 a 55) '), read(NumPres),
    write('Ingrese un número de acontecimiento (De 1 a 16) '), read(NumAcon),
    get_presidente(Num,Pres, _, _), acontecimiento_año(NumAcon, Acon, _), calcular_probabilidad(NumPres, NumAcon, Prob), P is round(Prob),
    write('La probabilidad de que el presidente '), write(Pres), write(' este involucrado en el acontecimiento '),
    write(Acon), write(' es de un del '), write(P), write('%.').

inflacion_presidente() :- write('Ingrese el número de presidente (De 1 a 55) '), read(NumPres),
    get_presidente(NumPres, Pres, _, H), Anio is round((H - 5)/ 10) * 10, tasa_inflacion_promedio_decada(Anio, Tasa),
    write('La tasa de inflación promedio del presidente '), write(Pres), write(' fue de '), write(Tasa), write('%.'). 


